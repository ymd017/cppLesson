#include <iostream>
using namespace std;

/*============================================================
* メイン関数
* 引数　：なし
* 戻り値：なし
============================================================*/
int main() {

	// 変数宣言部
	int num;

	// 入力
	cout << "1~9の整数を入力してください\n";
	cin >> num;

	try {
		if (num < 1 || num > 9) {
			throw "範囲外の数値です\n";
		}
	}

	catch (const char* err) {
		cout << "エラー：" << err << "\n";
	}
}