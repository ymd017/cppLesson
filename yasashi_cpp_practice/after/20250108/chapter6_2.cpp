#include <iostream>
using namespace std;

/*============================================================
* メイン関数
* 引数　：なし
* 戻り値：なし
============================================================*/
int main() {
	
	int n;
	int sum = 0;

	//テストの点数を入力し、点数を加算していって合計点を求める
	while (true) {
		cout << "テストの点数を入力してください。(0で終了)\n";
		cin >> n;

		//0が入力されたら、繰り返しを辞める
		if (n == 0) {
			break;
		}

		sum += n;
	}

	//合計点を出力する
	cout << "合計点は" << sum << "です。\n";
	
}
