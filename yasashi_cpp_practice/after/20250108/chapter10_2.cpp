#include <iostream>
using namespace std;

/*============================================================
* メイン関数
* 引数　：なし
* 戻り値：なし
============================================================*/
int main() {
	
	//動的なメモリを確保する
	int* pA;
	pA = new int;

	//動的なメモリに値を格納する
	*pA = 10;

	//出力する
	cout << *pA;

	//動的なメモリを解放する
	delete pA;
	
}
