#include <iostream>
using namespace std;

/*============================================================
* メイン関数
* 引数　：なし
* 戻り値：なし
============================================================*/
int main() {
	
	//要素型int、要素数5の配列を用意する
	int a[5];

	//先頭から5,4,3,2,1を代入する
	for (int i = 0; i < 5; i++) {
		a[i] = 5 - i;
	}

	//要素を順に表示する
	for (int i = 0; i < 5; i++) {
		cout << "a[" << i << "] = " << a[i] << "\n";
	}
}
