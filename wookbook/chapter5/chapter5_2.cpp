#include <iostream>
using namespace std;

/*============================================================
* メイン関数
* 引数　：なし
* 戻り値：なし
============================================================*/
int main()
{
	//要素型double,要素数が5の配列を宣言する
	double a[5];

	//配列の全要素に0.0を代入する
	for (int i = 0; i < 5; i++)
	{
		a[i] = 0.0;
	}

	//要素を出力する
	for (int i = 0; i < 5; i++)
	{
		cout << "a[" << i << "] = " << a[i] << "\n";
	}
}
