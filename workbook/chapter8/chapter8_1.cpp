#include <iostream>
#include <typeinfo>
using namespace std;

/*
* 問題文：
* 文字列リテラルの型と大きさを表示するプログラムを作成せよ。
*/

/*============================================================
* メイン関数
* 引数　：なし
* 戻り値：なし
============================================================*/
int main() {

	// 文字列リテラルの初期化
	char s[] = "abc";

	// 文字列リテラルの型と大きさの表示
	cout << "文字列リテラル：" << s << "\n";
	cout << "文字列リテラルの型：" << typeid(s).name() << "\n";
	cout << "文字列リテラルの大きさ：" << sizeof(s);
}


