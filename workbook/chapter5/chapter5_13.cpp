#include <iostream>
#include <typeinfo>
using namespace std;

/* 
* 問題文：
* 配列と要素の型情報をtypeid演算子によって表示するプログラムを作成せよ
*/

/*============================================================
* メイン関数
* 引数　：なし
* 戻り値：なし
============================================================*/
int main() {

	//配列を用意する
	int a[] = { 1,2,3,4,5 };

	//配列と要素の型情報をtypeid演算子で出力する
	cout << "配列の型： " << typeid(a).name() << "\n";
	cout << "配列の要素の型： " << typeid(a[0]).name() << "\n";
}